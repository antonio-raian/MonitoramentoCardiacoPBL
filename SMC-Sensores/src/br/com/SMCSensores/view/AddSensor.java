    //Metodo usado para alterar o valor do label na tela dependendo do valor do jSlider
    private String labelMovimento(){
        switch (slMove.getValue()){
            case 0:
                return "Repouso";
            case 1:
                return "Moderado";
            case 2:
                return "Esporte";
            default:
                return null;
        }
    }
    
