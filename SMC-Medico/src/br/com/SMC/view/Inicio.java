    //action do botão novo medico
    private void btnAddMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMedicoActionPerformed
        try {
            //Cria uma instancia para a tela de cadastro do medico
            CadastroMedico add = new CadastroMedico(this, false, txtEndereco.getText(),txtPorta.getText());
            add.setVisible(true);//Deixa-a visível
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível conectar ao servidor: "+txtEndereco.getText()+" na porta: "+porta);
        }
    }//GEN-LAST:event_btnAddMedicoActionPerformed

    //action do botão de login
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try{
            //Cria uma instancia para a tela de login do medico
            LoginMedico loginview = new LoginMedico(this, true, txtEndereco.getText(),txtPorta.getText());
            loginview.setVisible(true);//Torna-a visível
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, "Não foi possível conectar ao servidor: "+txtEndereco.getText()+" na porta: "+porta);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

